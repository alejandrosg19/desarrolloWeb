const elemento = document.querySelector(".contenedor")

/*firtChild()*/
let resultado = elemento.firstChild
console.log(resultado)

/*lastChild()*/
resultado = elemento.lastChild
console.log(resultado)

/*firstElementChild()*/
resultado = elemento.firstElementChild
console.log(resultado)

/*lastElementChild()*/
resultado = elemento.lastElementChild
console.log(resultado)

/*childNodes()*/
resultado = elemento.childNodes
console.log(resultado)

/*children()*/
resultado = elemento.children
console.log(resultado)